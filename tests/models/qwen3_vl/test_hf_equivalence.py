def _load_seeded_state_dict(model: Qwen3VLVisionModel) -> None:
    """Populate ``model`` with a deterministic state-dict, matching the offline HF run.

    The HF model's submodule construction order (norm1, norm2, attn, mlp) is mirrored
    by kornia, so iterating ``state_dict()`` in either gives the same key order. We
    fill each parameter from ``randn(shape, generator=Generator(seed=42))`` so the
    tensors are identical to the ones HF produced offline.
    """
    gen = torch.Generator().manual_seed(42)
    new_state: dict[str, torch.Tensor] = {}
    for key, tensor in model.state_dict().items():
        if "inv_freq" in key:
            new_state[key] = tensor
            continue
        new_state[key] = torch.randn(tensor.shape, generator=gen, dtype=torch.float32)
    model.load_state_dict(new_state)


def _seeded_input() -> tuple[torch.Tensor, torch.Tensor]:
    cfg = _TINY_CFG
    in_dim = cfg["in_channels"] * cfg["temporal_patch_size"] * cfg["patch_size"] ** 2
    n_tokens = 2 * 1 * 4 * 4
    gen = torch.Generator().manual_seed(0)
    patches = torch.randn(n_tokens, in_dim, generator=gen, dtype=torch.float32)
    grid_thw = torch.tensor([[1, 4, 4], [1, 4, 4]], dtype=torch.int64)
    return patches, grid_thw


