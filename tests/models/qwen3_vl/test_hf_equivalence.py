class TestQwen3VLHFEquivalence(BaseTester):
    def test_matches_official_implementation(self, device):
        model = Qwen3VLVisionModel(Qwen3VLVisionConfig(**_TINY_CFG)).to(device=device, dtype=torch.float32).eval()
        _load_seeded_state_dict(model)
        patches, grid_thw = _seeded_input()
        patches = patches.to(device)
        grid_thw = grid_thw.to(device)

        with torch.no_grad():
            out = model(patches, grid_thw)

        expected_main = _EXPECTED_MAIN.to(device)
        expected_ds0 = _EXPECTED_DS0.to(device)
        expected_ds1 = _EXPECTED_DS1.to(device)

        self.assert_close(out.last_hidden_state, expected_main, rtol=1e-4, atol=1e-4)
        assert len(out.deepstack_features) == 2
        self.assert_close(out.deepstack_features[0], expected_ds0, rtol=1e-4, atol=1e-4)
        self.assert_close(out.deepstack_features[1], expected_ds1, rtol=1e-4, atol=1e-4)
